      SUBROUTINE GLtest

      IMPLICIT NONE

      REAL anal
      REAL vsota
      INTEGER i      

C      Integrate[x^4, {x, -1, 1}]

      anal = 0.4D0
      vsota = 0.0D0
      DO i = 1,GLquad%n 
            vsota = vsota + GLquad%xi(i)*GLquad%xi(i)*GLquad%xi(i)*GLquad%xi(i)*GLquad%wi(i)
      END DO
   
      print *,"abs error = ",ABS(vsota-anal)

      END SUBROUTINE
